// Global Translation Helper (Upgraded with Fallback support)
window.t = function(key, fallbackText = "") {
    const lang = localStorage.getItem('mnotes_lang') || 'el';
    if (typeof TRANSLATIONS !== 'undefined' && TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
        return TRANSLATIONS[lang][key];
    }
    // Αν δεν βρει τη μετάφραση στο λεξικό, δείχνει το fallbackText (για να μη σπάσει το UI)
    return fallbackText || key;
}